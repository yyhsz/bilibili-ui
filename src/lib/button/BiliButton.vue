<template>
  <button class="bili-button" :class="classes">
    <slot></slot>
  </button>
</template>

<script>
import { computed } from "vue";

export default {
  props: {
    theme: {
      type: String,
      default: "button",
    },
    size: {
      type: String,
      default: "medium",
    },
    level: {
      type: String,
      default: "normal",
    },
    disabled: {
      type: Boolean,
      default: false,
    },
    loading: {
      type: String,
      default: "false",
    },
  },
  setup(props, ctx) {
    const { theme, loading, size, level, disabled } = props;
    const classes = computed(() => {
      return {
        [`iz-theme-${theme}`]: theme,
        [`iz-size-${size}`]: size,
        [`iz-level-${level}`]: level,
        loading,
        disabled,
      };
    });

    return { classes };
  },
};
</script>

